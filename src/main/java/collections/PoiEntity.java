package collections;

public class PoiEntity {

	private int i;
	private String city;
	private float lon;
	private float lan;
	
	
	
	
	public int getI() {
		return i;
	}
	public void setI(int i) {
		this.i = i;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public float getLon() {
		return lon;
	}
	public void setLon(float lon) {
		this.lon = lon;
	}
	public float getLan() {
		return lan;
	}
	public void setLan(float lan) {
		this.lan = lan;
	}
	
	
	
	
}
